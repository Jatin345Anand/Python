print('Hello There')       # Single quotes

print("Hello 'Ravi'")      #...or use double quotes

print("\"Hello\" Ravi ")

print("Hello my name is \n Ravi")       # \n is for newline

print(r"Hello my name is \n Ravi")      # use r before String to print full String with special characters

print("""\
void main(){
    int a, b;
    a=10;
    b=10;
    int c = a+b;
""")                # To print multiple lines use """...""" or '''...'''

print("Hello "*5)   # String is repeated using *

print("Hello "*2 + "Ravi")  # String is concatenated with +

print('lap' 'top')       # Two or more string literals next to each other are automatically concatenated.

prefix = "lap"
print(prefix + "top")      #  to concatenate variables or a variable and a literal, use +:



