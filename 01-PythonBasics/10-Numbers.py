print(2+1)      # Sum

print(2*3)      # Multiply

print(20-10)    # Subtraction

print(17/3)     # Division  ( division always returns a floating point number )

print(17//3)    # floor division discards the fractional part

print(20%3)     # % will return remainder of the division

print(5**2)     # Squared

print(5**5)     # 5 to the power of 5
